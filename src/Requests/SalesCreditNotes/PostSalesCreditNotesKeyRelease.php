<?php

declare(strict_types=1);

namespace ChrisJohnLeah\SageAccounting\Requests\SalesCreditNotes;

use ChrisJohnLeah\SageAccounting\Data\SalesCreditNote;
use Saloon\Contracts\Body\HasBody;
use Saloon\Enums\Method;
use Saloon\Http\Request;
use Saloon\Http\Response;
use Saloon\Traits\Body\HasJsonBody;

/**
 * Releases a Sales Credit Note
 *
 * POST /sales_credit_notes/{key}/release
 */
class PostSalesCreditNotesKeyRelease extends Request implements HasBody
{
    use HasJsonBody;

    protected Method $method = Method::POST;

    /**
     * @param  array<string, mixed>  $payload
     */
    public function __construct(
        private readonly string $key,
        private readonly array $payload = [],
    ) {
    }

    public function resolveEndpoint(): string
    {
        return str_replace('{key}', rawurlencode($this->key), '/sales_credit_notes/{key}/release');
    }

    /**
     * @return array<string, mixed>
     */
    protected function defaultBody(): array
    {
        return $this->payload;
    }

    public function createDtoFromResponse(Response $response): ?SalesCreditNote
    {
        $data = $response->json();

        if (! is_array($data)) {
            return null;
        }

        /** @var array<string, mixed> $data */
        return SalesCreditNote::fromArray($data);
    }
}
